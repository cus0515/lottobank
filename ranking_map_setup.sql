-- 월간 랭킹, 명예의 전당, 판매점 지도 데이터를 저장하는 확장 스키마

alter table public.tickets
  add column if not exists lottery_type text default 'lotto',
  add column if not exists purchase_channel text default 'retail',
  add column if not exists purchase_mode text,
  add column if not exists matched_count integer,
  add column if not exists total_number_count integer default 6,
  add column if not exists store_id text,
  add column if not exists store_name text,
  add column if not exists store_address text,
  add column if not exists store_lat double precision,
  add column if not exists store_lng double precision;

create table if not exists public.lottery_stores (
  id text primary key,
  name text not null,
  address text not null,
  region text,
  lat double precision,
  lng double precision,
  scan_count bigint not null default 0,
  first_scanned_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.ranking_snapshots (
  id bigint generated by default as identity primary key,
  period_key text not null,
  ranking_type text not null,
  user_id uuid not null references auth.users(id) on delete cascade,
  rank integer not null check (rank > 0),
  score numeric not null default 0,
  title text not null,
  badge_icon text,
  border_color text,
  created_at timestamptz not null default now(),
  unique (period_key, ranking_type, user_id)
);

create table if not exists public.user_daily_visits (
  user_id uuid not null references auth.users(id) on delete cascade,
  visit_date date not null default current_date,
  first_visited_at timestamptz not null default now(),
  last_visited_at timestamptz not null default now(),
  primary key (user_id, visit_date)
);

create table if not exists public.user_system_titles (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  title_type text not null,
  title text not null,
  lottery_type text,
  round_no integer,
  prize_rank integer,
  period_key text,
  badge_icon text,
  border_color text,
  active boolean not null default true,
  awarded_at timestamptz not null default now(),
  expires_at timestamptz,
  unique (user_id, title_type, title, period_key)
);

create index if not exists idx_ranking_snapshots_period
  on public.ranking_snapshots (period_key, ranking_type, rank);
create index if not exists idx_user_system_titles_active
  on public.user_system_titles (user_id, active, awarded_at desc);
create index if not exists idx_lottery_stores_location
  on public.lottery_stores (lat, lng);
create index if not exists idx_tickets_ranking_fields
  on public.tickets (purchase_date, user_id, prize_rank, store_id);

alter table public.lottery_stores enable row level security;
alter table public.ranking_snapshots enable row level security;
alter table public.user_system_titles enable row level security;
alter table public.user_daily_visits enable row level security;

drop policy if exists "lottery stores are public" on public.lottery_stores;
create policy "lottery stores are public"
  on public.lottery_stores for select using (true);

drop policy if exists "ranking snapshots are public" on public.ranking_snapshots;
create policy "ranking snapshots are public"
  on public.ranking_snapshots for select using (true);

drop policy if exists "system titles are public" on public.user_system_titles;
create policy "system titles are public"
  on public.user_system_titles for select using (true);

drop policy if exists "users manage own daily visits" on public.user_daily_visits;
create policy "users manage own daily visits"
  on public.user_daily_visits for all
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);
