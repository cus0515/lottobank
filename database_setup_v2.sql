  prize_rank integer,
  prize_amount bigint not null default 0,
  lottery_type text not null default 'lotto',
  purchase_channel text not null default 'retail',
  purchase_mode text,
  matched_count integer,
  total_number_count integer not null default 6,
  user_store_id uuid references public.user_lottery_stores(id) on delete set null,
  store_id text,
  store_name text,
  store_address text,
  store_lat double precision,
  store_lng double precision,
  created_at timestamptz not null default now()
