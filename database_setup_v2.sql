create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, nickname)
  values (new.id, coalesce(new.raw_user_meta_data ->> 'nickname', split_part(new.email, '@', 1)))
  on conflict (id) do nothing;
  insert into public.user_stats (user_id) values (new.id) on conflict (user_id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

do $$
declare
  table_name text;
  policy_name text := 'LottoBank 관리자 전체 권한';
begin
  foreach table_name in array array[
    'profiles','lottery_stores','user_lottery_stores','tickets','user_stats','user_badges',
    'activity_feed','chat_messages','posts','comments','post_likes','ranking_snapshots',
    'user_system_titles','user_daily_visits'
  ]
  loop
    execute format('alter table public.%I enable row level security', table_name);
    execute format('drop policy if exists %I on public.%I', policy_name, table_name);
    execute format(
      'create policy %I on public.%I for all to authenticated using ((select auth.jwt() ->> ''email'') = ''jobdevil23@gmail.com'') with check ((select auth.jwt() ->> ''email'') = ''jobdevil23@gmail.com'')',
      policy_name,
      table_name
    );
  end loop;
end
$$;

